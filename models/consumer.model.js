const mongoose = require("mongoose");

const consumerSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    consumptions: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Consumption",
      },
    ],
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("Consumer", consumerSchema);
